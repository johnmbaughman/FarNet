namespace FarNet;

/// <summary>
/// Editor expand tabs mode.
/// </summary>
public enum ExpandTabsMode
{
	/// <summary>
	/// Tabs are not replaced with spaces.
	/// </summary>
	None,

	/// <summary>
	/// All tabs are replaced with spaces.
	/// </summary>
	All,

	/// <summary>
	/// Only new tabs are replaced with spaces.
	/// </summary>
	New
}
