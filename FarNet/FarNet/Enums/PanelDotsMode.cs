namespace FarNet;

/// <summary>
/// Panel dots item modes.
/// </summary>
public enum PanelDotsMode
{
	/// <summary>
	/// Add the dots item if the panel is a child panel.
	/// </summary>
	Auto,

	/// <summary>
	/// Always add the dots item.
	/// </summary>
	Dots,

	/// <summary>
	/// No dots item.
	/// </summary>
	Off
}
